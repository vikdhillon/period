
      FUNCTION PERIOD_POLY (COEFF, NPOLY, X)

C============================================================================
C            NPOLY
C Evaluates   SUM COEFF(I) * X**(I-1).
C             I=1
C
C Written by Vikram Singh Dhillon @LPO 21-August-1993.
C============================================================================

	IMPLICIT NONE	

	INTEGER NPOLY
	DOUBLE PRECISION  PERIOD_POLY, COEFF(NPOLY), X
	DOUBLE PRECISION  X0,X1,X2,X3,X4,X5,X6,X7,X8,X9
        DOUBLE PRECISION  X10,X11,X12,X13,X14,X15,X16,X17,X18,X19

	X0  = COEFF(1)
	X1  = COEFF(2)  * (X)
	X2  = COEFF(3)  * (X**2.0D0)
	X3  = COEFF(4)  * (X**3.0D0)
	X4  = COEFF(5)  * (X**4.0D0)
	X5  = COEFF(6)  * (X**5.0D0)
	X6  = COEFF(7)  * (X**6.0D0)
	X7  = COEFF(8)  * (X**7.0D0)
	X8  = COEFF(9)  * (X**8.0D0)
	X9  = COEFF(10) * (X**9.0D0)
	X10 = COEFF(11) * (X**10.0D0)
	X11 = COEFF(12) * (X**11.0D0)
	X12 = COEFF(13) * (X**12.0D0)
	X13 = COEFF(14) * (X**13.0D0)
	X14 = COEFF(15) * (X**14.0D0)
	X15 = COEFF(16) * (X**15.0D0)
	X16 = COEFF(17) * (X**16.0D0)
	X17 = COEFF(18) * (X**17.0D0)
	X18 = COEFF(19) * (X**18.0D0)
	X19 = COEFF(20) * (X**19.0D0)

	IF (NPOLY .EQ. 1) THEN
		PERIOD_POLY = X0
	ELSE IF (NPOLY .EQ. 2) THEN
		PERIOD_POLY = X0+X1
	ELSE IF (NPOLY .EQ. 3) THEN
		PERIOD_POLY = X0+X1+X2
	ELSE IF (NPOLY .EQ. 4) THEN
		PERIOD_POLY = X0+X1+X2+X3
	ELSE IF (NPOLY .EQ. 5) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4
	ELSE IF (NPOLY .EQ. 6) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5
	ELSE IF (NPOLY .EQ. 7) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6
	ELSE IF (NPOLY .EQ. 8) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7
	ELSE IF (NPOLY .EQ. 9) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8
	ELSE IF (NPOLY .EQ. 10) THEN  
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9
	ELSE IF (NPOLY .EQ. 11) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10
	ELSE IF (NPOLY .EQ. 12) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11
	ELSE IF (NPOLY .EQ. 13) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12
	ELSE IF (NPOLY .EQ. 14) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12+X13
	ELSE IF (NPOLY .EQ. 15) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12+X13
     +                        +X14
	ELSE IF (NPOLY .EQ. 16) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12+X13
     +                        +X14+X15
	ELSE IF (NPOLY .EQ. 17) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12+X13
     +                        +X14+X15+X16
	ELSE IF (NPOLY .EQ. 18) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12+X13
     +                        +X14+X15+X16+X17
	ELSE IF (NPOLY .EQ. 19) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12+X13
     +                        +X14+X15+X16+X17+X18
	ELSE IF (NPOLY .EQ. 20) THEN
		PERIOD_POLY = X0+X1+X2+X3+X4+X5+X6+X7+X8+X9+X10+X11+X12+X13
     +                        +X14+X15+X16+X17+X18+X19
	END IF

	RETURN
	END
